# SUM OF TWO NUMBERS
num1  = input("input first number")
num2  = input("input second number")


firstNumber = int(num1)
secondNumber = int(num2)



sum = firstNumber + secondNumber
print("Sum of {0} and {1} is {2}" .format(firstNumber, secondNumber, sum)) 


# DIFF OF TWO NUMBERS
diff= firstNumber - secondNumber 
print("Diff of {0} and {1} is {2}" .format(firstNumber, secondNumber, diff))

# PRODUCT OF TWO NUMBERS	
prod = firstNumber * secondNumber
print("Prod of {0} and {1} is {2}" .format(firstNumber, secondNumber, prod))

# DIVISON OF TWO NUMBERS	
div = firstNumber  / secondNumber
print("Div of {0} and {1} is {2}" .format(firstNumber, secondNumber, div))